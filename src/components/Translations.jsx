// Helper hook to use translations
export function useTranslation(user) {
  // Priority 1: Check URL parameter (for immediate post-reload)
  let lang = null;
  if (typeof window !== 'undefined') {
    const urlParams = new URLSearchParams(window.location.search);
    lang = urlParams.get('lang');
  }

  // Priority 2: Get from user object
  if (!lang) {
    lang = user?.preferred_language;
  }

  // Priority 3: Check localStorage for explicit language setting
  if (!lang && typeof window !== 'undefined') {
    lang = localStorage.getItem('helper33_language');
  }

  // Priority 4: Try user cache (which might contain preferred_language or a general 'language' field)
  if (!lang && typeof window !== 'undefined') {
    try {
      const cachedUser = localStorage.getItem('helper33_user_cache');
      if (cachedUser) {
        const userData = JSON.parse(cachedUser);
        lang = userData?.user?.preferred_language || userData?.language;
      }
    } catch (e) {
      // Ignore cache errors (e.g., malformed JSON, storage not available)
    }
  }

  // Final fallback to English
  lang = lang || 'en';

  // Validate language exists in translations, fallback to 'en' if not
  if (!TRANSLATIONS[lang]) {
    console.warn(`Language '${lang}' not found in translations, falling back to English.`);
    lang = 'en';
  }

  // Log for debugging
  // console.log('useTranslation - Detected language:', lang, 'from user:', user?.preferred_language); // Uncomment for verbose debugging

  const t = (path) => {
    const keys = path.split('.');
    let value = TRANSLATIONS[lang];

    for (const key of keys) {
      value = value?.[key];
      if (!value) {
        // Fallback to English if translation not found for specific path
        value = TRANSLATIONS.en;
        for (const k of keys) {
          value = value?.[k];
        }
        break;
      }
    }

    return value || path;
  };

  // Return language code and RTL status as well for convenience
  return { t, lang, isRTL: lang === 'ar' };
}

// Helper function for components that don't use hooks
export function translate(lang, path) {
  const keys = path.split('.');
  let value = TRANSLATIONS[lang] || TRANSLATIONS.en; // Fallback to EN if the main lang object is missing

  for (const key of keys) {
    value = value?.[key];
    if (!value) {
      // Fallback to English if translation not found for specific path
      value = TRANSLATIONS.en;
      for (const k of keys) {
        value = value?.[k];
      }
      break;
    }
  }

  return value || path;
}

